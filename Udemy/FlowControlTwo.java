
public class FlowControlTwo {

	public static void main(String[] args) {
		
			String x = "Two";		// can run it with strings too	
			int a = 1;              // if you remove 'break', everything gets printed
			
			switch(a) {
			
			case 0: System.out.println("The value of a is 0"); 
			break;
			
			case 1: System.out.println("The value of a is 1");
			break;
			
			case 2: System.out.println("The value of a is 2");
			break;
			
			case 3: System.out.println("The value of a is 3");
			break;
			
			default: System.out.println("None of the above");
			}
	}

}
